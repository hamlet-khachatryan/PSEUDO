def get_atom_radius(
    element: str,
    charge: int,
    resolution: float,
    radii_table: Optional[Dict[Tuple[str, int], List[float]]] = None,
) -> float:
    """
    Look up the resolution-dependent electron density sphere radius

    Performs linear interpolation between the two nearest resolution
    breakpoints in the table. Clamps to boundary values outside the
    parametrized range [0.5, 3.0] A

    Args:
        element: Element symbol
        charge: Formal charge on the atom (e.g., 0, 2, -1)
        resolution: Map resolution
        radii_table: Override table. If None, uses the built-in RADII_TABLE

    Returns:
        Interpolated radius
    """
    table = radii_table if radii_table is not None else RADII_TABLE

    key = (element, charge)
    if key not in table:
        key = (element, 0)
    if key not in table:
        return get_default_radius(element, resolution)

    radii = table[key]
    return _interpolate_radius(radii, resolution)


def get_default_radius(
    element: str,
    resolution: float,
) -> float:
    """
    Fallback radius for elements not in the radii table

    Uses the gemmi covalent radius as a base estimate and scales it
    by a resolution-dependent factor derived from the behavior of
    common elements (C, N, O) in the table

    Args:
        element: Element symbol
        resolution: Map resolution in Angstroms

    Returns:
        Estimated radius in Angstroms
    """
    try:
        el = gemmi.Element(element)
        cov_r = el.covalent_r
    except (RuntimeError, ValueError):
        cov_r = 0.77  # fallback to carbon covalent radius

    # Scale factors derived from C(0) radii: radius / cov_r at each breakpoint
    scale_factors = [1.325, 1.481, 1.636, 1.792, 2.143, 2.571]
    scale = _interpolate_radius(scale_factors, resolution)
    return cov_r * scale


def _interpolate_radius(radii: List[float], resolution: float) -> float:
    """
    Linearly interpolate a radius value between resolution breakpoints

    Args:
        radii: List of 6 radius values at the standard breakpoints
        resolution: Target resolution
    Returns:
        Interpolated radius value
    """
    bps = RESOLUTION_BREAKPOINTS

    if resolution <= bps[0]:
        return radii[0]
    if resolution >= bps[-1]:
        return radii[-1]

    for i in range(len(bps) - 1):
        if bps[i] <= resolution <= bps[i + 1]:
            frac = (resolution - bps[i]) / (bps[i + 1] - bps[i])
            return radii[i] + frac * (radii[i + 1] - radii[i])

    return radii[-1]
